#include "CTile.h"
//---------------------------------------------------------------------------
CTile::CTile(){

    TileID = 0;
    TypeID = TILE_TYPE_NONE;

    X = 0.0;
    Y = 0.0;
}
//---------------------------------------------------------------------------
 CTile::CTile(int X, int Y, int ID, int type){

    TileID = ID;
    TypeID = type;

    this->X = X;
    this->Y = Y;

}
